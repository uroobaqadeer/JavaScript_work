var color=prompt("What's your favorite color of traffic signal?");
if(color ==="red"){
    document.write("Signal color ","<b>",color," </b> Must Stop")
}
else if(color === "yellow"){
    document.write("Signal color ","<b>",color," </b>  Ready to move");
}
else if(color === "green"){
    document.write("Signal color ","<b>",color," </b>  Move now");
}
